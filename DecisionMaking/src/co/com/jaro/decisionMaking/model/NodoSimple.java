package co.com.jaro.decisionMaking.model;

public class NodoSimple {

	String valor;
	int fi;
	int ffi;
	float hi;
	float hhi;
	double varianza;
	
	public double getVarianza() {
		return varianza;
	}
	public void setVarianza(double d) {
		this.varianza = d;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	public int getFi() {
		return fi;
	}
	public void setFi(int fi) {
		this.fi = fi;
	}
	public int getFfi() {
		return ffi;
	}
	public void setFfi(int ffi) {
		this.ffi = ffi;
	}
	public float getHi() {
		return hi;
	}
	public void setHi(float hi) {
		this.hi = hi;
	}
	public float getHhi() {
		return hhi;
	}
	public void setHhi(float hhi) {
		this.hhi = hhi;
	}
	
}
